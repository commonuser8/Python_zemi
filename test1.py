# コメントアウト(javaの//)
''' 複数行のコメントアウト(javaの/* */) '''

# 標準出力
print("Hello World")  # System.out.println("Hello World");
print('Hello World')
print(1)

# 複数文字列の出力
print("Hello" + "World")

# 数字の出力
print(1 + 2)
print("1" + "2")
print("1 + 2")

# 改行せずに出力
print("Hello World", end="")  # System.out.print("Hello World");

print("Hello World")

# 改行コマンド
print("Hello\nWorld")  # macは \r, unixは \n, windowsは \r\n